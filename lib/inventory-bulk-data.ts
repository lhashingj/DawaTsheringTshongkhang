export function autoUnit(name: string): string {
  const n = name.toUpperCase();
  if (/METER|MTR|ROLL|PIPE \d|HOSE/.test(n)) return 'MTR';
  if (/KG|LBS|GRAM|GM/.test(n)) return 'KG';
  if (/LTS|LITRES|LT /.test(n)) return 'LTR';
  if (/SET|KIT|PCS SET|PICES SET|PIECE SET/.test(n)) return 'SET';
  if (/BOX/.test(n)) return 'BOX';
  return 'EACH';
}

export function toSKU(name: string): string {
  return name
    .replace(/[^A-Z0-9]/gi, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '')
    .toUpperCase()
    .slice(0, 40);
}
